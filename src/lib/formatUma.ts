// ウマの表示用フォーマット。
// 慣用の「○-○」表記（2位のウマ-1位のウマ。例: [30,10,-10,-30] → "10-30"）。
// 上下対称なウマのときだけ成立するので、非対称な手入力ルールは
// 全要素を並べた "30/10/-10/-30" 表記にフォールバックする。
export function formatUma(uma: number[]): string {
  if (uma.length === 4 && uma[0] === -uma[3] && uma[1] === -uma[2]) {
    return `${uma[1]}-${uma[0]}`;
  }
  return uma.join("/");
}
