-- ================================================
-- 卓を「複数卓のときだけ」持つようにする
-- ================================================
-- 背景:
--   4人・単一卓の対局では卓の登録・選択が純粋な摩擦になる。卓が意味を持つのは
--   同じ回戦で複数卓が同時に立つときだけ。そこで単一卓の対局は table_id を持た
--   ない（null）ことを許し、アプリ側は卓が2つ以上あるときだけ卓 UI を出す。
--
--   複合 FK matches(tournament_id, table_id) -> tables(tournament_id, id) は
--   MATCH SIMPLE（既定）なので、table_id が null の行は検査がスキップされ通る。
--   よって NOT NULL を外すだけでよい。
-- ================================================
alter table matches alter column table_id drop not null;
